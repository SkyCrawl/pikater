    public void setAgentOptions(List options) {

        System.err.println("SETAGENTOPTIONS");

        this.options = options;
        optionsPanel.removeAll();
        optionPanels.clear();

        for (int i = 0; i < options.size(); i++) {
            OptionPanel op = new OptionPanel((Option)options.get(i), getAgentType());
            optionPanels.add(op);
            optionsPanel.add(((Option)options.get(i)).getName(), op);
        }

        optionsPanel.revalidate();

    }

    public List getAgentOptions() {
        options.clear();
        for (int i = 0; i < optionPanels.size(); i++) {
            if (optionPanels.get(i).getOption() != null)
                options.add(optionPanels.get(i).getOption());
        }
        return options;
    }

    public String getAgentType() {
        return jComboBox1.getSelectedItem().toString();
    }

